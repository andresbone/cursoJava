public class OperadoresAsignacion {
    public static void main(String[] args) {
        System.out.println("*** Operadores de asignacion ***");

        // Asignacion ( = )
        var numero0 = 10; // El operador asigna el valor (10) a la variable convirtiendo la variable en entera
        int numero; // Variable no declarada

        // Asignacion compuesto
        // +=, -=, *=, /=, %=
        numero0 += 5;
        System.out.println("numero0 = " + numero0);
        numero0 *= 2;
        System.out.println("numero0 = " + numero0);


        


    }
}
